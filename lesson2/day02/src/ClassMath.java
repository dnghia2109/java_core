
public class ClassMath {
    public static void main(String[] args) {
        System.out.println("pi: " + Math.PI);
        System.out.println("Số lớn nhất trong 2 số: " + Math.max(3, 9));
        System.out.println("Số bé nhất trong 2 số: " + Math.min(3, 9));
        System.out.println("Số ngẫu nhiên: " + Math.random());
        System.out.println("Khai căn: " + Math.sqrt(25));
        System.out.println("Lũy thừa: " + Math.pow(5, 3));
    }
}
