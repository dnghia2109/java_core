public class Calculator {
    public void sum(int a, int b){
        System.out.println("a + b = " + (a+b));
    }

    public void sum(float a, float b){
        System.out.println("a + b = " + (a+b));
    }

    public void sum(int a, int b, int c){
        System.out.println("a + b + c = " + (a+b+c));
    }


}
